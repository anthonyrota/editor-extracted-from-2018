export function isExtendedPictographic(codePoint: number): boolean {
  return getValueFromUnicodeTrieData(codePoint) === 1
}
function getValueFromUnicodeTrieData(codePoint: number): number {
  if (codePoint < 0 || codePoint > 0x10ffff) {
    return 0
  }

  if (codePoint < 0xd800 || (codePoint > 0xdbff && codePoint <= 0xffff)) {
    // Ordinary BMP code point, excluding leading surrogates.
    // BMP uses a single level lookup.  BMP index starts at offset 0 in the index.
    // data is stored in the index array itself.
    const index = (data[codePoint >> 5] << 2) + (codePoint & 31)
    return data[index]
  }

  if (codePoint <= 0xffff) {
    // Lead Surrogate Code Point.  A Separate index section is stored for
    // lead surrogate code units and code points.
    //   The main index has the code unit data.
    //   For this function, we need the code point data.
    const index =
      (data[2048 + ((codePoint - 0xd800) >> 5)] << 2) + (codePoint & 31)
    return data[index]
  }

  if (codePoint < 131072) {
    // Supplemental code point, use two-level lookup.
    let index = data[2080 + (codePoint >> 11)]
    index = data[index + ((codePoint >> 5) & 63)]
    index = (index << 2) + (codePoint & 31)
    return data[index]
  }

  return data[data.length - 4]
}

